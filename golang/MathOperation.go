package main

import "fmt"

func main() {
	// Math Operation
	var result = 15 + 15
	fmt.Println(result)

	// Augmented Assignments
	var count = 1
	count += 5
	fmt.Println(count)

	// Unary Operator
	var i = 1
	i++
	fmt.Println(i)

	var negatif = -100
	var positif = +100
	fmt.Println(negatif)
	fmt.Println(positif)
}